# 2-4. Name Cases:
new_name = "Elzer Mokoena"

print(new_name.lower()) # Returns a copy the newName data as lowercase

print(new_name.upper()) # returns a copy of the newName variable to upper case

new_name = new_name.lower() # changes the value of the new variable to lower case
print(new_name) # returns the newName variable

print(new_name.title()) # returns a copy of the newName in title case